int main(void)
{
	int ret;
	bool led_state = true;

	if (!gpio_is_ready_dt(&led)) {
		return 0;
	}

	ret = gpio_pin_configure_dt(&led, GPIO_OUTPUT_ACTIVE);
	if (ret < 0) {
		return 0;
	}

	ret = flash_erase(dev, MAIN_BASE_ADDRESS, 2048);
	if (ret < 0) {
		printk("Failed to erase flash %d\n", ret);
		return ret;
	}

	ret = flash_write(dev, MAIN_BASE_ADDRESS, &write_data[0], strlen(write_data));
	if (ret < 0) {
		printk("Failed to write flash %d\n", ret);
		return ret;
	}

	ret = flash_read(dev, MAIN_BASE_ADDRESS, &read_data[0], sizeof(read_data));
	if (ret < 0) {
		printk("Failed to read data from flash %d\n", ret);
		return ret;
	}

	printk("Read data = %s\n", read_data);

	while (1) {
		ret = gpio_pin_toggle_dt(&led);
		if (ret < 0) {
			return 0;
		}

		led_state = !led_state;
		printf("LED state: %s\n", led_state ? "ON" : "OFF");
		k_msleep(SLEEP_TIME_MS);
	}
	return 0;
}

