#ifndef ZEPHYR_DRIVERS_BC12_BQ25620_H_
#define ZEPHYR_DRIVERS_BC12_BQ25620_H_

#define BQ2562X_IOTG_LSB         0x0a
#define BQ2562X_IOTG_MSB         0x0b
#define BQ2562X_VOTG_LSB         0x0c
#define BQ2562X_VOTG_MSB         0x0d
#define BQ2562X_TIMER_CTRL       0x15
#define BQ2562X_CHRG_CTRL_3      0x18
#define BQ2562X_CHRG_FLAG_1      0x21

/* REG0x0A_IOTG */
#define BQ2562X_IOTG_MIN	100000
#define BQ2562X_IOTG_MAX	2400000
#define BQ2562X_IOTG_STEP_UA	20000
#define BQ2562X_IOTG_SHIFT	4
#define BQ2562X_IOTG_MSB_MSK	GENMASK(3, 0)
#define BQ2562X_IOTG_LSB_MSK	GENMASK(7, 4)

/* REG0X0C_VOTG*/
#define BQ2562X_VOTG_MIN	3840000
#define BQ2562X_VOTG_MAX	9600000
#define BQ2562X_VOTG_STEP_UV	80000
#define BQ2562X_VOTG_SHIFT	6
#define BQ2562X_VOTG_MSB_MSK	GENMASK(4, 0)
#define BQ2562X_VOTG_LSB_MSK	GENMASK(7, 6)

/* REG0x15_Charger_Timer_Control */
#define BQ2562X_TIMER_CTRL_MSK		GENMASK(7, 0)
#define BQ2562X_TIMER_DIS_STAT		BIT(7)
#define BQ2562X_TIMER_AUTO_INDET	BIT(6)
#define BQ2562X_TIMER_FRCE_INDET	BIT(5)
#define BQ2562X_TIMER_EN_DCP		BIT(4)
#define BQ2562X_TIMER_TMR2X_EN		BIT(3)
#define BQ2562X_TIMER_SAFE_TMRS		BIT(2)
#define BQ2562X_TIMER_PRECHR_TMR	BIT(1)
#define BQ2562X_TIMER_CHG_TMR		BIT(0)


/* REG0x18_Charger_Control_3 */
#define BQ2562X_CTRL3_EN_OTG 	BIT(6)

/* REG0x21_Charger_Flag_1 */
#define BQ2562X_VBUS_FLAG_MSK	BIT(0)
#define BQ2562X_CHG_FLAG_MSK	BIT(3)

#define BQ2562X_POWER_NONE	0
#define BQ2562X_SDP_CURR_UA	500000
#define BQ2562X_CDP_CURR_UA	1500000
#define BQ2562X_DCP_CURR_UA	1500000
#define BQ2562X_VOLT_UV		5040000

enum bq25620_mode {
	BQ2562X_POWER_DOWN,
	BQ2562X_SDP_HOST_MODE,
	BQ2562X_CDP_HOST_MODE,
	BQ2562X_DCP_HOST_MODE,
};

#endif /* ZEPHYR_DRIVERS_BC12_BQ25620_H_ */
